pvTable doBuild(pvKey cKey,bool enc)
{
	pvTable vt = boost::make_shared<vTable>();

	unsigned int dwKey[32 + 4] = { 0 };
	unsigned int *psKey = (unsigned int *)sKey;
	unsigned int *pcKey = (unsigned int *)cKey->v;
	 
	for (int i = 0; i < 4; i++)
		dwKey[i] = psKey[i] ^ pcKey[i];

	for (int i = 0; i < 0x20; i++) {
		unsigned int v = dwKey[i + 1] ^ dwKey[i + 2]
			^ dwKey[i + 3] ^ dsTab[i];
		v = doXTab(v);

		dwKey[i + 4] = v ^ dwKey[i];

		if (enc) {
			vt->v[i] = dwKey[i + 4];
		}
		else {
			vt->v[31 - i] = dwKey[i + 4];
		}
	}

	return vt;
}

pvKey buildKey(const char *mem)
{
    pvKey vk = boost::make_shared<vKey>();

    memcpy(vk->v, mem, 16);

    return vk;
}